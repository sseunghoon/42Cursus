#include "libft.h"

void	ft_bzero(void *p, size_t n)
{
	size_t	i;
	char	*arr;

	arr = (char *)p;
	i = 0;
	while (i < n)
	{
		arr[i] = 0;
		i++;
	}
	return ;
}
