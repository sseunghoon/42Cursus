#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void*))
{
	t_list	*p;
	t_list	*q;

	p = *lst;
	while (p)
	{
		q = p->next;
		if (p->content)
			del(p->content);
		free(p);
		p = q;
	}
	*lst = NULL;
}
